// dynamic type language -- JavaScript
//primitive type -- numbers, string, boolean, null, undefined

const a = 5;
const b = 'Kopa samsu';  
const c = true;
const ages = [45, 65, 58];
const student = {id: 23, class: 7}
console.log(typeof a, typeof b, typeof c, typeof ages, typeof student);


//Non-primitive type -- Objects, arrays

let x = 5;
let y = 5;
console.log(x, y);

y = 7;
console.log(x, y);